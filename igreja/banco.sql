CREATE DATABASE IF NOT EXISTS igreja CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE igreja;

CREATE TABLE IF NOT EXISTS membros (
    id INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    foto VARCHAR(255) DEFAULT NULL,
    nome_completo VARCHAR(150) NOT NULL,
    sexo ENUM('M','F') DEFAULT NULL,
    data_nascimento DATE DEFAULT NULL,
    nacionalidade VARCHAR(80) DEFAULT NULL,
    naturalidade VARCHAR(80) DEFAULT NULL,
    estado_uf CHAR(2) DEFAULT NULL,
    escolaridade VARCHAR(30) DEFAULT NULL,
    profissao VARCHAR(100) DEFAULT NULL,
    identidade VARCHAR(30) DEFAULT NULL,
    cpf VARCHAR(20) DEFAULT NULL,
    pai VARCHAR(150) DEFAULT NULL,
    mae VARCHAR(150) DEFAULT NULL,
    estado_civil VARCHAR(30) DEFAULT NULL,
    conjuge VARCHAR(150) DEFAULT NULL,
    filhos INT DEFAULT 0,
    rua VARCHAR(150) DEFAULT NULL,
    numero VARCHAR(20) DEFAULT NULL,
    bairro VARCHAR(80) DEFAULT NULL,
    cep VARCHAR(20) DEFAULT NULL,
    cidade VARCHAR(80) DEFAULT NULL,
    estado CHAR(2) DEFAULT NULL,
    telefone VARCHAR(25) DEFAULT NULL,
    tipo_ingresso VARCHAR(20) DEFAULT NULL,
    data_decisao DATE DEFAULT NULL,
    procedencia VARCHAR(150) DEFAULT NULL,
    congregacao VARCHAR(150) DEFAULT NULL,
    area VARCHAR(50) DEFAULT NULL,
    nucleo VARCHAR(50) DEFAULT NULL,
    observacao TEXT DEFAULT NULL,
    criado_em DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP
);
